function mostraIdade(nome, idade) {
    return `A idade de ${nome} é ${idade} anos.`;
}

function mostraCidade(nome, cidade) {
    return `${nome} reside em ${cidade}`;
}

function mostraHobby(hobby) {
    return `e o seu hobby favorito é ${hobby}.`;
}

export {mostraIdade, mostraCidade, mostraHobby};
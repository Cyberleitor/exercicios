print('Para calcular o volume de uma caixa retangular, primeiro informe o comprimento...')
comprimento = float(input())
print('Agora, informe a largura...')
largura = float(input())
print('Por fim, informe a altura...')
altura = float(input())

volume = comprimento * largura * altura

print('Diante das informações dadas, calculo que o volume da caixa retangular é: ', volume)

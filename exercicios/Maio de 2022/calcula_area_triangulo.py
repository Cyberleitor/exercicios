print('Vamos calcular a área de um triângulo.')
print('Informe o tamanho da base do triângulo...')
base = float(input())
print('Agora, informe a altura do triângulo...')
altura = float(input())

area = (base * altura) / 2

print('Diante dos dados fornecidos, calculo que a área do triângulo corresponde a ' + str(area) + '.')